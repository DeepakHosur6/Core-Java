class Puma{
	//For static varibles Declaration and Initialization varibles at same time...
	static String brand ="Puma";
	static String colour ="Light Gray";
	static String material ="leather";
	static String size ="UK9";
	static int price =7999;
	
	public static void main (String args[])
	{
		System.out.println(" Brand       :"+brand);
		System.out.println(" Colour      :"+colour);
		System.out.println(" Material    :"+material);
		System.out.println(" Model Number:"+size);
		System.out.println(" Price       :"+price);
	}
}
